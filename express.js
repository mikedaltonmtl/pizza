const express = require("express");
const morgan = require('morgan');
const path = require('path');

////////////////////////////////////////////////////////////////////////////////
// Configuration
////////////////////////////////////////////////////////////////////////////////
const app = express();
const PORT = 8000;

////////////////////////////////////////////////////////////////////////////////
// Middleware
////////////////////////////////////////////////////////////////////////////////
app.use(express.urlencoded({ extended: true }));
app.use(morgan('dev'));

////////////////////////////////////////////////////////////////////////////////
// Route Handlers - GET
////////////////////////////////////////////////////////////////////////////////
app.get('/', (req, res) => {
  res.render('./index');
  // res.send("Hello World!");

});

////////////////////////////////////////////////////////////////////////////////
// Listener
////////////////////////////////////////////////////////////////////////////////
app.listen(PORT, () => {
  console.log(`Express app listening on port ${PORT}!`);
});